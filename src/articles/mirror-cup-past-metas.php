       <h3 class="article-header">Tempest Cup</h3>
<p>
	<strong><a href="https://pvpoke.com/rankings/tempest/1500/overall/" target="_blank">Tempest Cup Rankings</a></strong><br>
  <strong>Types: </strong>Flying, Ground, Ice,  Electric <br>
<strong>Balance:</strong>&nbsp; &#9733; &#9733; &#9733; &#9734;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9734; &#9734; &#9734;<br>
  <strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>Hydro Cannon Swampert is crashing this cup with no       survivors</li>
      </ul>
      <p>Tempest Cup is almost exactly as you remember  it; Lanturn is zapping things up, Lapras is playing hide-and-seek with  Abomasnow and Tropius, and Skarmory is shredding them into energy batteries.  Classic Skarmory! Like in Boulder Cup, Hydro Cannon Swampert now gives the  other mudbois a run for their money. <strong>Swampert can beat or tie the former  frontrunners</strong>, Whiscash and Quagsire, and plays  Skarmory much closer. When it comes down to it, Whiscash may still end up being  preferred for the late-game Blizzard. </p>
      <p>Not necessarily a matchup change, but now that  many more people have access to Ice Shard Lapras, I&rsquo;d anticipate seeing it much  more than Sealeo in a revamped Tempest Cup.</p>
	<a name="kingdom"></a>
<h3 class="article-header">Kingdom Cup</h3>
<p>
	<strong><a href="https://pvpoke.com/rankings/kingdom/1500/overall/" target="_blank">Kingdom Cup Rankings</a></strong><br>
  <strong>Types: </strong>Ice, Fire, Steel, Dragon <br>
<strong>Balance:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9734; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9734; &#9734; &#9734;<br>
  <strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>Blaziken gets a speed boost</li>
        <li>A viable budget Bastiodon replacement in Probopass</li>
        <li>Bronzong locks down previously iffy matchups and gains       valuable coverage</li>
      </ul>
      <p><strong>The infamous BALL core is likely still intact</strong> (Bastiodon, Altaria, Lucario, Lapras), but some of the wild card  picks are more up in the air. Blaziken, who can beat or go even with 3/4 of the  core, now has access to Blaze Kick. Where before it had to be well positioned  to reach even just one Charged Move, <strong>Blaziken can pump out more consistent  damage and do some shield bait trickery if it&rsquo;s brave</strong>. There are a variety  of possible movesets; Blaze Kick and Blast Burn offer the most flexibility and  power but get walled by Altaria. Brave Bird can do some neutral chunk damage,  and Focus Blast still looks really good as a late game nuke against Lapras or  Bastiodon. </p>
      <p>One big problem with the Kingdom Cups of yore  was the accessibility of some of its most powerful Pokemon. Bastiodon had  people digging up fossils as well as Stardust from under their couch cushions. <strong>Probopass  is now available as a genuine Bastiodon replacement. </strong>Before, teams could  roll with Steelix, Bronzong, or other options but never quite fill that  triceratops-shaped gap. Probopass is functionally nearly identical to Bastiodon  and won&rsquo;t send your Stardust funds back to the Cretacious. </p>
      <p>Bronzong was an interesting pick with a unique  set of matchups, but those matches were somewhat haphazard. <strong>Bronzong can now  consistently beat Lapras in the 1-shield scenario with Psyshock spam</strong>. Its  Altaria matchup stays close but positive with Psyshock, too. Psyshock also  packs a greater punch than Heavy Slam against Lucario if you can land it.  (Generally you just want to spam Confusion against Lucario, and there&rsquo;s even a <a href="https://pvpoke.com/battle/1500/bronzong-26.5-3-1-8-4-4-1/lucario/11/0-0-0/1-4-3/">breakpoint</a> you might be able to hit!) </p>

<h3 class="article-header">Season 1 Regionals</h3>
	<strong><a href="https://pvpoke.com/rankings/regionals-1/1500/overall/" target="_blank">Season 1 Regionals Rankings</a></strong><br>
  <strong>Types: </strong>Rock, Ground, Fighting, Ice, Fire, Steel, Dragon, Poison, Dark, Fairy, Ghost, Flying, Electric<br>
<strong>Balance:</strong>&nbsp; &#9733; &#9733; &#9733; &#9734;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9733; &#9733; &#9734;<br>
  <strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>Don't sleep on Jirachi</li>
        <li>More people might run Swampert or a mudboi</li>
        <li>Charm is doing Charm things</li>
      </ul>
      <p>Like open Great League, the Season 1 Regional meta could see a significant shift with the introduction of Charm. There's more to keep Medicham up at night and Sableye is sweating ghost sweats. How exactly Charm will impact the meta remains to be seen but it opens up an entire new coverage option that wasn't really present before. <strong>Wigglytuff and Clefable are the top Charm candidates</strong>, with Wigglytuff's unique resistance to Ghost and Clefable's Meteor Mash. In response, I think Skarmory's place will solidify and Altaria will fall out almost completely. If you didn't have it on your team previously, a Bastiodon or Probopass can also provide helpful anti-Charm support.</p>
	<p>As Poison could potentially rise, Jirachi enters the fray as a top contendor with positive matchups across the field. There were quite a few Confusion options last time between Hypno and Gallade, and while Jirachi does slightly different things, I think it's definitely worth a look. Meanwhile, not everyone packed a Water/Ground Pokemon last time, but Swampert could draw some attention, too.</p>
	<p>This section deserves more analysis, but the Regionals/open format is also one of the more challenging to unpack. We'll see how it shakes out!</p>
	<a name="nightmare"></a>
      <h3 class="article-header">Nightmare Cup</h3>
		<p>
			<strong><a href="https://pvpoke.com/rankings/nightmare/1500/overall/" target="_blank">Nightmare Cup Rankings</a></strong><br>
        <strong>Types: </strong>Fighting, Dark, Psychic <br>
        <strong>Additional Rules: </strong>No Sableye, Medicham, legendaries, or mythicals <br>
<strong>Balance:</strong>&nbsp; &#9733; &#9734; &#9734; &#9734;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9733; &#9733; &#9733;<br>
        <strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>So funny story</li>
        <li>Charm Gardevoir annihilates everything</li>
        <li>Run Double Dark team comps at your peril</li>
        <li>Lucario potentially over Toxicroak</li>
      </ul>
      <p>If you think Vigoroth is the most dominant meta  Pokemon we&rsquo;ve ever seen, get ready for this. Charm Gardevoir has a <a href="https://pvpoke.com/battle/multi/1500/nightmare/gardevoir/11/1-1-3/2-1/" target="_blank">91% win rate</a> in the 1-shield scenario and a <a href="https://pvpoke.com/battle/multi/1500/nightmare/gardevoir/22/1-1-3/2-1/" target="_blank">92% win rate</a> in the 2-shield scenario. Its top  counters start with a Battle Rating of 660. And after it&rsquo;s done wiping the  floor with your poor opponent, your pick of Dazzling Gleam or Shadow Ball will  either force a shield, one-shot, or severely cripple whichever Pokemon comes in  to face it. In case it bears repeating, <strong>Charm Gardevoir annihilates (almost) everything.</strong></p>
      <p>So what beats Gardevoir? <strong>Bronzong is your  best bet </strong>with either Confusion or Feint Attack, followed by Metagross and  Metang. Confusion Bronzong does the job while giving it much better matchups  against Toxicroak and Lucario. With Feint Attack, you have the edge against  Metagross and Metang, so there are some coverage decisions to be made. <strong>Dark/Poisons  also play close or even with Gardevoir</strong> and can squeak out potential wins  depending on IV&rsquo;s, so it&rsquo;s not all doom and gloom. <strong>Bite Drapion</strong> has the best matchup there and should beat Gardevoir through sheer bite force.</p>
      <p>These Pokemon are in turn beaten by Dark-type  staples like Umbreon and Alolan Raticate. Fighting Pokemon can carve out a spot  on the roster against Gardevoir counters and Gardevoir counter-counters. <strong>Lucario  emerges as a favorite over Toxicroak</strong> for its ability to go even with  Bronzong and hardened resistances against Poison and Steel. Toxicroak can also  still succeed but may have a much tougher time since the traditional  Fighting-Dark-Psychic trio has broken down. </p>
      <p>Where are all the other Nightmare players  floating around? <strong>Hypno is still looking strong </strong>with Fire Punch and  Shadow Ball giving it multiple super effective options against some of the  threats above. Note that Gardevoir can take down Hypno through sheer Charm  damage, and Hypno can neither Fire Punch + Shadow Ball or Shadow Ball + Shadow  Ball without an energy advantage. </p>
      <p>Xatu and Alolan Raichu are in a similar boat  where they&rsquo;re fairly well rounded but lose to much of the apparent new meta,  such as Gardevoir, the Steel types, and Dark/Poisons. Taking on Lucario and its  Shadow Ball is also risky business! Shiftry is also looking pretty shifty these  days. </p>
      <p>And lastly, what about Spiritomb? Charm is an  obvious threat alongside all Dark types, so I think <strong>Spiritomb will have a  much tougher time finding a roster spot</strong>. It could act as a scarecrow to  force apart an otherwise strong pairing between Bronzong and Lucario. (Claydol  is also looking on with hungry eyes). </p>

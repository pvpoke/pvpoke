<h3 class="article-header">Boulder Cup</h3>
	<p>
		<strong><a href="https://pvpoke.com/rankings/boulder/1500/overall/" target="_blank">Boulder Cup Rankings</a></strong><br>
<strong>Types: </strong>Steel, Ground, Fighting,  Rock <br>
<strong>Balance:</strong>&nbsp; &#9733; &#9734; &#9734; &#9734;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>More accessible Skarmory counters</li>
        <li>Power-Up Punch Medicham can go toe to toe with Skarmory and dominate 2-shield matchups</li>
        <li>Hydro Cannon Swampert competes for the Mudboi slot</li>
        <li>New Confusion users look to even the playing field</li>
      </ul>
      <p>What humble beginnings! Those who remember  Boulder Cup probably remember the double Skarmory menace. That can no longer be a thing with the species clause, but <strong>Skarmory is still  top bird</strong> with a heap of positive matchups. Countering Skarmory used to mean  investing in Melmetal, finding a legacy Magneton, or opting for Steelix, but  now <strong>Probopass </strong>and <strong>Magnezone </strong>are also within reach. <strong>Bastiodon </strong>is  an option, too, but all of these alternatives will have a very hard time  against Medicham and the mudbois. </p>
      <p>Medicham was a big player before, and it didn&rsquo;t  even have Power-Up Punch back then. Now, <strong>Medicham is a wrecking ball</strong> that only has 3 losses in the 2-shield scenario: Torterra, Charm Gallade, and  Charm Donphan. <strong>Charm is the only way to hard counter Medicham,</strong> and it&rsquo;s  a risky proposition with all the Steel types about, leaving these Pokemon  vulnerable to being hard countered themselves. </p>
      <p>Marshtomp and Whiscash were previous favorites  in the Water/Ground category, with relatively solid matchups across the cup and  acting as Skarmory&rsquo;s bodyguard. <strong>Hydro Cannon Swampert takes over that role</strong> with insane damage and a generally even matchup with Skarmory itself. Some may  still prefer Whiscash for its bulk and ability to threaten its Grass counters  with Blizzard. </p>
      <p>A bunch of new Confusion users are also looking  to mix things up, including Bronzong, Gallade, and Jirachi.  They are each well rounded in their own right and pick up some important  matchups, including Torterra and Medicham. <strong>Gallade can provide additional  Grass support </strong>(but will need backup to deal with Swampert) while <strong>Bronzong  and Jirachi have an even Skarmory matchup </strong>and can deal with Grass better  thanks to their Steel typing. With these newcomers, we can probably kiss  Breloom goodbye, too.</p>
		<a name="twilight"></a>
  <h3 class="article-header">Twilight Cup</h3>
	<strong><a href="https://pvpoke.com/rankings/twilight/1500/overall/" target="_blank">Twilight Cup Rankings</a></strong><br>
  <strong>Types: </strong>Dark, Ghost, Fairy,  Poison <br>
<strong>Balance:</strong>&nbsp; &#9733; &#9733; &#9733; &#9733;<br>
<strong>Accessibility:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
<strong>Change:</strong>&nbsp; &#9733; &#9733; &#9734; &#9734;<br>
  <strong>Key Changes:</strong></p>
      <ul type="disc">
        <li>Acid Spray will keep everyone on their toes</li>
        <li>Charm Togekiss gets to be the fairy that Togetic always       wanted to be</li>
        <li>Earth Power makes Nidoqueen more viable</li>
      </ul>
      <p>Twilight Cup should look mostly familiar. There  aren&rsquo;t many big new players, but the old ones have some new tricks. During  Twilight Cup, stat boosts were just becoming a thing and Silver Wind was all  the rage. Now it&rsquo;s joined by Acid Spray, which throws a curveball at the entire  cup. <strong>Tentacruel can now bait shields</strong>, making it even more potent than  before, and <strong>Alolan Muk is strongly positioned in the Dark/Poison category  with hit and run tactics.</strong> Acid Spray could be devastating when paired with  high damage moves like Razor Leaf or Charm. Acid Spray is also an option on  Qwilfish, especially in the lead position, but some may prefer Sludge Wave to  target Azumarill. </p>
      <p>So many Twilight Cup rosters were hungry for a  Fairy Fast Move, and now we have it! <strong>Togekiss stands out with coverage moves  against Golbat, Venomoth, and Venusaur. </strong>Charm also shreds through the  Twilight staple, Toxcroak (but watch out for Sludge Bomb). Granbull and  Gardevoir are also on the table if you want to avoid Rock or Ice weaknesses,  but come with their own drawbacks as well. Charm users could potentially be  used in place of traditional Toxicroak counters like Golbat and Venomoth thanks  to their strength against Dark types. Consider your team and coverages  carefully! </p>
      <p>Lastly, while Nidoqueen was more of a B-team  option for those without Toxicroak, Earth Power gives it some more oomph this  time around. It&rsquo;s more capable of taking on Poison targets in shielded  scenarios now, and Stone Edge continues to give it valuable coverage. Perhaps  worth considering!</p>
